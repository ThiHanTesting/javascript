let a = 3
a += 2  // a = a + 2
console.log(a) // 5

a -= 4  // a = a - 5
console.log(a) // 1

a *= 8 // a = a * 8
console.log(a) // 8

a /= 2 // a = a / 2
console.log(a) // 4

a %= 2 // a = a % 2
console.log(a) // 0



